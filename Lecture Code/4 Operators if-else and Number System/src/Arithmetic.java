public class Arithmetic {
        public static void main(String[] args){

            int a = 8;
            int b = 5;
            System.out.println("a+b: "+(a+b));
            System.out.println("a-b: "+(a-b));
            System.out.println("a*b: "+(a*b));
            System.out.println("a/b: "+(a/b));
            System.out.println("a%b: "+(a%b));

            double x = 5;
            double y = 2;
            System.out.println("x+y: "+(x + y));
            System.out.println("x-y: "+(x - y));
            System.out.println("x*y: "+(x * y));
            System.out.println("x/y: "+(x / y));
            System.out.println("x%y: "+(x % y));

        }
}
