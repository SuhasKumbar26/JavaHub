public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello 'Java' Developers.............");
        System.out.println("Hello \"Java\" Developers...........'");
        System.out.println("Hello \\Java\\ Developers...........'");
        System.out.println("Hello \nJava Developers...........'");
        System.out.println("Hello \nJava Developers...........'");
        System.out.println("Hello \tJava Developers...........'");
        System.out.println("Hello \bJava Developers...........'");
    }
}
